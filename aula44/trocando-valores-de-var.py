
'''Para trocar o valor de variaveis: basta, primeiro indicar quais variaveis IRÃO receber o valor de outras variaveis, e depois indicar
RESPECTIVAMENTE quais variaveis SERÃO aquelas que irão se TORNAR o valor das variaveis indicadas primariamente.'''

g = 'o'
u = 'v'
s = 'a'
t = 't'
a = 's'
v = 'u'
o = 'g'

g, u, s, t, a, v, o = o, v, a, t, s, u, g

